-- Function to calculate player's age based on their DOB and the current date

function GetPlayerAge(card_id)
    -- Check if player has a DOB, otherwise use fallback DOB
    local dob = players[card_id] or "04/01/08"  -- Fallback DOB for players without one
    
    local birthDay, birthMonth, birthYear = string.match(dob, "(%d%d)/(%d%d)/(%d%d)")

    local currentDay, currentMonth, currentYear = string.match(GLOBAL_DATE_PLACEHOLDER, "(%d%d)/(%d%d)/(%d%d)")

    -- Convert to numeric for calculations
    if tonumber(birthYear) < 30 then
        birthYear = tonumber("20" .. birthYear)  -- For 2000-2099 (20th century)
    else
        birthYear = tonumber("19" .. birthYear)  -- For 1900-1999 (19th century)
    end

    currentYear = tonumber("20" .. currentYear)  -- Assuming 21st century for current year

    local age = currentYear - birthYear
    if currentMonth < birthMonth or (currentMonth == birthMonth and currentDay < birthDay) then
        age = age - 1
    end

    return age
end

function computeTransferValue(age, rating, position, playerIndex, replacementCount, daysLeftInWindow)
  local MAX_VALUE = 200000000  -- €200M cap
  local MIN_VALUE = 100000     -- Minimum transfer fee

