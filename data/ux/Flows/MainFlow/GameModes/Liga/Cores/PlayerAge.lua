  local base = 5000000 + (rating - 50) * 500000  -- Starting value based on rating
  local ageFactor
  if age <= 20 then ageFactor = 2.5
  elseif age <= 23 then ageFactor = 2.0
  elseif age <= 26 then ageFactor = 1.8
  elseif age <= 29 then ageFactor = 1.5
  elseif age <= 32 then ageFactor = 1.0
  else ageFactor = math.max(0.3, 0.6 - (age - 32) * 0.1) end  -- Decline for older players
  
  -- Position multipliers for market demand
  local positionMultipliers = {
    ["GK"] = 1.0, ["LB"] = 1.1, ["RB"] = 1.1, ["CB"] = 1.2, ["RCB"] = 1.2, ["LCB"] = 1.2,
