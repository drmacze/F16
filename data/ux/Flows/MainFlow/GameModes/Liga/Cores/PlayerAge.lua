  if age < 25 then contractFactor = 1.2  -- Younger players assumed to have longer contracts
  elseif age <= 30 then contractFactor = 1.0
  else contractFactor = 0.8 end          -- Older players nearing contract end
  -- Removed random variation: contractFactor = contractFactor * (0.9 + math.random() * 0.2)
  -- Now fixed at the base contractFactor value

  -- 3. Market Factor (Timing and Demand)
  local marketFactor = 1.0
  if daysLeftInWindow < 7 then
    marketFactor = 1.15  -- Fixed late-window premium (was 1.1 + math.random() * 0.2)
  end
  if position == "ST" or position == "CAM" then
    marketFactor = marketFactor + 0.05  -- Extra demand for key positions
  end

  -- 4. Club Factor (Player Importance and Replacements)
  local clubFactor = 1.0
  if playerIndex <= 11 then
    clubFactor = 1.2  -- Starters are more valuable
  else
    clubFactor = 0.8 - (playerIndex - 11) * 0.05  -- Reserves lose value
  end
  if replacementCount > 2 then
    clubFactor = clubFactor * 0.9  -- More replacements, less reluctance to sell
  end

  -- 5. Compute Final Asking Price
  local finalValue = baseValue * contractFactor * marketFactor * clubFactor
  finalValue = math.min(finalValue, MAX_VALUE)  -- Cap at max value

  -- Round to nearest million if >= 1M, otherwise nearest thousand
  if finalValue >= 1000000 then
    finalValue = math.floor(finalValue / 1000000 + 0.5) * 1000000  -- Round to nearest million
  else
    finalValue = math.floor(finalValue / 1000 + 0.5) * 1000  -- Round to nearest thousand
  end

  -- 6. Compute Minimum Acceptable Value
  local minFraction = 0.7 + (clubFactor - 1.0) * 0.1  -- Ties willingness to sell to club factor
  local minAcceptableValue = finalValue * minFraction
  -- Apply same rounding logic to minAcceptableValue
  if minAcceptableValue >= 1000000 then
    minAcceptableValue = math.floor(minAcceptableValue / 1000000 + 0.5) * 1000000  -- Round to nearest million
  else
    minAcceptableValue = math.floor(minAcceptableValue / 1000 + 0.5) * 1000  -- Round to nearest thousand
  end
  minAcceptableValue = math.max(MIN_VALUE, minAcceptableValue)  -- Ensure a reasonable minimum

  return finalValue, minAcceptableValue
end

function daysLeftInTransferWindow(dateStr)
    dateStr = dateStr or "15/03/25"
    local day, month, year = dateStr:match("(%d%d)/(%d%d)/(%d%d)")
    if not day then return 0 end
    
    day, month, year = tonumber(day), tonumber(month), tonumber(year)
    local fullYear = 2000 + year
    local daysInMonth = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31}
    local currentDayOfYear = day
    for i = 1, month - 1 do currentDayOfYear = currentDayOfYear + daysInMonth[i] end
    
    local summerDeadlineDay, winterDeadlineDay = 242, 30
    local summerYear = currentDayOfYear <= summerDeadlineDay and fullYear or fullYear + 1
    local winterYear = fullYear
    local daysToSummer = fullYear == summerYear and summerDeadlineDay - currentDayOfYear or (365 - currentDayOfYear) + summerDeadlineDay + (summerYear - fullYear - 1) * 365
    local daysToWinter = currentDayOfYear <= winterDeadlineDay and winterDeadlineDay - currentDayOfYear or (365 - currentDayOfYear) + winterDeadlineDay + (winterYear == fullYear and 0 or 365)
    return math.min(math.max(0, daysToSummer), math.max(0, daysToWinter))
end

function playerReplacementCount(teamID, position)
  -- Ensure teamID and position are provided
  if not teamID or not position then
    print("[playerReplacementCount] Error: teamID or position missing")
    return 0  -- Default to 0 replacements if inputs are invalid
  end

  -- Fetch the current lineup
  local players = getCachedTeamPlayers(teamID)
  local replacementCount = 0

  -- Check if lineup data is valid
  if not players or type(players) ~= "table" then
    print("[playerReplacementCount] Warning: No valid player data for teamID " .. teamID)
    return 0
  end

  -- Count replacements among substitutes (index 12+)
  for index = 12, #players do
    local player = players[index]
    if player and player.position == position then
      replacementCount = replacementCount + 1
    end
  end

  return replacementCount
end

