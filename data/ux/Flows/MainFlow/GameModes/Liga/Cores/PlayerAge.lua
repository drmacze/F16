    ["CM"] = 1.4, ["RM"] = 1.5, ["LM"] = 1.5, ["CAM"] = 1.8, ["RW"] = 1.9, ["LW"] = 1.9,
    ["ST"] = 2.0, ["CF"] = 2.0, ["RF"] = 1.8, ["LF"] = 1.8
  }
  local positionFactor = positionMultipliers[position] or 1.0
  
  -- Potential premium for young stars
  local potentialPremium = (age < 23 and rating >= 85) and 1.2 or 1.0
  local baseValue = base * ageFactor * positionFactor * potentialPremium

